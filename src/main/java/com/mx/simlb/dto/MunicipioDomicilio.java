package com.mx.simlb.dto;

public class MunicipioDomicilio {

	private Long idLocalidadDomicilio;
	private Long idCatLocalidad;
	private Long idCatDomicilio;
	
	public Long getIdLocalidadDomicilio() {
		return idLocalidadDomicilio;
	}
	public void setIdLocalidadDomicilio(Long idLocalidadDomicilio) {
		this.idLocalidadDomicilio = idLocalidadDomicilio;
	}
	public Long getIdCatLocalidad() {
		return idCatLocalidad;
	}
	public void setIdCatLocalidad(Long idCatLocalidad) {
		this.idCatLocalidad = idCatLocalidad;
	}
	public Long getIdCatDomicilio() {
		return idCatDomicilio;
	}
	public void setIdCatDomicilio(Long idCatDomicilio) {
		this.idCatDomicilio = idCatDomicilio;
	}
	
	
}
